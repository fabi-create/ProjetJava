package models;

public class RecipeDetails {
	
	private String item;
	private String info;
	
	public RecipeDetails() {
		
	}
	
	public String getItem() {
		return item;
	}
	
	public void setItem(String item) {
		this.item = item;
	}
	
	public String getInfo() {
		return info;
	}
	
	public void setInfo(String info) {
		this.info = info;
	}
}
